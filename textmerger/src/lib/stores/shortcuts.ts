import { writable } from 'svelte/store';

export interface Shortcuts {
    open: string;
    save: string;
    exit: string;
    remove: string;
    removeAll: string;
    copyText: string;
    refresh: string;
}

const DEFAULT_SHORTCUTS: Shortcuts = {
    open: 'Ctrl+O',
    save: 'Ctrl+S',
    exit: 'Ctrl+Q',
    remove: 'Ctrl+D',
    removeAll: 'Ctrl+Shift+D',
    copyText: 'Ctrl+Alt+C',
    refresh: 'F5',
};

function createShortcutsStore() {
    // Load from localStorage if available
    const stored = localStorage.getItem('textmerger_shortcuts');
    const initial: Shortcuts = stored ? { ...DEFAULT_SHORTCUTS, ...JSON.parse(stored) } : DEFAULT_SHORTCUTS;

    const { subscribe, set, update } = writable<Shortcuts>(initial);

    return {
        subscribe,
        updateShortcut: (action: keyof Shortcuts, keybind: string) => {
            update(s => {
                const newShortcuts = { ...s, [action]: keybind };
                localStorage.setItem('textmerger_shortcuts', JSON.stringify(newShortcuts));
                return newShortcuts;
            });
        },
        resetDefaults: () => {
            set(DEFAULT_SHORTCUTS);
            localStorage.setItem('textmerger_shortcuts', JSON.stringify(DEFAULT_SHORTCUTS));
        }
    };
}

export const shortcuts = createShortcutsStore();

